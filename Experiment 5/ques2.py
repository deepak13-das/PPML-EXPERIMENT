# List
my_list = [10, 20, 30]
print("List elements:")
for item in my_list:
    print(item)

# Tuple
my_tuple = (1, 2, 3)
print("\nTuple elements:")
for item in my_tuple:
    print(item)

# Dictionary
my_dict = {"a": 1, "b": 2, "c": 3}
print("\nDictionary elements:")
for key, value in my_dict.items():
    print(key, ":", value)

# Set
my_set = {100, 200, 300}
print("\nSet elements:")
for item in my_set:
    print(item)
